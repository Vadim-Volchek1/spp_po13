# Исходный список чисел (может быть любым)
numbers = [1, 2, 2, 3, 4, 4, 5, 1, 6]

# Множество автоматически хранит только уникальные значения
unique_numbers = set(numbers)

print("Уникальные числа:", unique_numbers)

# Если нужен отсортированный вывод
print("Отсортированные уникальные числа:", sorted(unique_numbers))
